        <div class="box b1" data-anim="1">
            <h2>Sélection des ingrédients</h2>
            <p>Malt (céréales germées), Eau, Houblon (une plante qui apporte l'amertume et le goût à la bière).</p>
        </div>
        <div class="box b2" data-anim="2">
            <h2>Concassage</h2>
            <p>Broyage du malt en fines particules pour exposer l'amidon contenu à l'intérieur.</p>
        </div>
        <div class="box b3" data-anim="3">
            <h2>Empâtage</h2>
            <p>Mélange du malte concassé avec l'eau chaude dans la cuve.</p>
        </div>
        <div class="box b4" data-anim="4">
            <h2>Filtration</h2>
            <p>Séparation de la partie liquide (le moût) de la bière et de la partie solide (les enveloppes du malte, aussi connu sous le nom de drêche).</p>
        </div>
        <div class="box b5" data-anim="5">
            <h2>Ébulition</h2>
            <p>Le moût est porté à ébullition, ce qui va permettre de le stériliser. Puis, on ajoute le houblon.</p>
        </div>
        <div class="box b6" data-anim="6">
            <h2>Fermentation</h2>
            <p>Les levures transforment les sucres du moût en alcool et en gaz. Cela joue un rôle essentiel dans le développement de ses arômes.</p>
        </div>
        <div class="box b7" data-anim="7">
            <h2>Garde (fermentation secondaire)</h2>
            <p>La fermentation secondaire prend la suite de la fermentation primaire. Elle va permettre d'affiner et de stabiliser la bière.</p>
        </div>
        <div class="box b8" data-anim="8">
            <h2>Embouteillage</h2>
            <p>La bière, une fois fermentée, maturée et clarifiée, est conditionnée dans des bouteilles.</p>
        </div>
        <div class="box b9" data-anim="9">
            <h2>Refermentation en bouteille</h2>
            <p>Ajout de sucre après l'embouteillage pour créer naturellement du gaz et enrichir les arômes de la bière.</p>
        </div>
